package loops;

public class DoWhile {
	
	public static void main(String[] args) {
		
		// initilization   condition   execution  increment/decrement
		
		// initilization  execution    increment/decrement   condition
		
		//needs to execute single statement even condition fails
		
		int i=10; //initilization
		
		do {
			System.out.println(i);
			i++;
		}while(i<10);
	}

}
